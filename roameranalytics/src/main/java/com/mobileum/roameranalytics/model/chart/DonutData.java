package com.mobileum.roameranalytics.model.chart;

/**
 * The Class DonutData.
 *
 * @author sarvesh
 */
public class DonutData {

	/** The label. */
	private String label;
	
	/** The value. */
	private double value;
	
	private double roamers;
	
	private double mo;
	
	private double mt;
	
	private double data;
	/**
	 * @return the label
	 */
	public String getLabel() {
		return label;
	}
	/**
	 * @param label the label to set
	 */
	public void setLabel(String label) {
		this.label = label;
	}
	/**
	 * @return the value
	 */
	public double getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(double value) {
		this.value = value;
	}
	/**
	 * @return the roamers
	 */
	public double getRoamers() {
		return roamers;
	}
	/**
	 * @param roamers the roamers to set
	 */
	public void setRoamers(double roamers) {
		this.roamers = roamers;
	}
	/**
	 * @return the mo
	 */
	public double getMo() {
		return mo;
	}
	/**
	 * @param mo the mo to set
	 */
	public void setMo(double mo) {
		this.mo = mo;
	}
	/**
	 * @return the mt
	 */
	public double getMt() {
		return mt;
	}
	/**
	 * @param mt the mt to set
	 */
	public void setMt(double mt) {
		this.mt = mt;
	}
	/**
	 * @return the data
	 */
	public double getData() {
		return data;
	}
	/**
	 * @param data the data to set
	 */
	public void setData(double data) {
		this.data = data;
	}
}
